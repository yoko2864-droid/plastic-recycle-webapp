# --- Tkinter GUI ---
def show_info(event=None):
    selected_item = combo.get()
    if not selected_item:
        return
    info_text.delete("1.0", tk.END)
    for item in plastic_data:
        if item["物品"] == selected_item:
            for key, value in item.items():
                info_text.insert(tk.END, f"{key}: {value}\n")
            break

def search_item():
    keyword = search_entry.get().strip()
    if not keyword:
        messagebox.showinfo("提示", "請輸入搜尋關鍵字")
        return
    results = [item["物品"] for item in plastic_data if keyword in item["物品"]]
    if results:
        combo["values"] = results
        combo.set(results[0])
        show_info()
    else:
        messagebox.showinfo("搜尋結果", "找不到符合的物品")
        combo.set("")
        info_text.delete("1.0", tk.END)

root = tk.Tk()
root.title("塑膠分類查詢")
root.geometry("600x500")

# 搜尋欄
tk.Label(root, text="搜尋物品:").pack(pady=5)
search_frame = tk.Frame(root)
search_frame.pack()
search_entry = tk.Entry(search_frame, width=30)
search_entry.pack(side=tk.LEFT, padx=5)
search_button = tk.Button(search_frame, text="搜尋", command=search_item)
search_button.pack(side=tk.LEFT)

# 下拉選單
tk.Label(root, text="選擇物品:").pack(pady=5)
combo = ttk.Combobox(root, width=50, values=[item["物品"] for item in plastic_data])
combo.pack()
combo.bind("<<ComboboxSelected>>", show_info)

# 顯示資訊
info_text = tk.Text(root, height=20, width=70)
info_text.pack(pady=10)

root.mainloop()
